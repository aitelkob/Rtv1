#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int n;

	if (s == NULL)
		return ;
	n = ft_strlen(s);
	i = 0;
	while (i < n)
	{
		s[i] = '\0';
		i++;
	}
}
