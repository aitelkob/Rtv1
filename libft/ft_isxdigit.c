#include "libft.h"

int	ft_isxdigit(int c)
{
	if (c >= 48 && c <= 57)
		return (1);
	else if ((c >= 'A' && c <= 'F') || (c >= 'a' && c <= 'f'))
		return (1);
	return (0);
}
