#include "libft.h"

int	ft_isgraph(int c)
{
	if ((c >= 33 && c <= 47) || (c >= 58 && c <= 126))
		return (1);
	if ((char)c >= 48 && (char)c <= 57)
		return (1);
	else
		return (0);
}
